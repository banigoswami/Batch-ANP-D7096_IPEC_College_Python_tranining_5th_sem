'''
---------------------------------------------------------Voting Eligibility--------------------------------------------------------
Check whether a person is eligible to vote or not. A person is eligible to vote if his/her age is above or equal to 18 years.
-----------------------------------------------------------------------------------------------------------------------------------
Sample Input:
Enter your age (in years): 20
----------------------------------------------------------------
Sample Output:
You are not eligible to vote
--------------------------Coding--------------------------------
'''
age = int(input("Enter your age (in years): "))
#-------------Validating the age----------------
if age < 0:
    exit("Enter a positive value")
#-----------------------------------------------
if age >= 18:
    print("-------------------------------------------")
    print("You are eligible to vote")
    print("-------------------------------------------")
else:
    print("-------------------------------------------")
    print("You are not eligible to vote")
    print("-------------------------------------------")
#----------------------------------------------------------------------------------------------------------------------------------